n = input('digite algo: ')

print(n,'é letras manhusculas? ', n.isupper())
print(n,'é letras? ', n.isalpha())
print(n,'é numeros? ', n.isnumeric())
print(n,'é letras e numeros? ', n.isalnum())
print(n,'é numero decimal? ', n.isdecimal())
print(n,'é digito? ', n.isdigit())
print(n,'é letras minusculas? ', n.islower())
print(n,'é espaço? ', n.isspace())

